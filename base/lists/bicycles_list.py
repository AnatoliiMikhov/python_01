# A simple python list
# python list methods
bicycles = ["trek", "cannondale", "redline", "specialized"]
print(bicycles)

# print first element of the list
first_bicycle = bicycles[0]
print(f"First bicycle in the list is {first_bicycle.title()}")

# print last element
last_elem = bicycles[-1]
print(f"Last bicycle in the list is: {last_elem.title()}")
